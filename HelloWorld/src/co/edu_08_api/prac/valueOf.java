package co.edu_08_api.prac;
// 문자열 변환
public class valueOf {
	public static void main(String[] args) {
		String str1 = String.valueOf(10); // String -> int
		String str2 = String.valueOf(10.5); // String -> double
		String str3 = String.valueOf(true); // String -> boolean
		
		/*
		 * boolean
		 * char
		 * int
		 * long
		 * double
		 * float
		 */

		System.out.println(str1);
		System.out.println(str2);
		System.out.println(str3);
	}

}
